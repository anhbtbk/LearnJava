public class Bai1 {
    //Chú ý tên class luôn bắt đầu bằng 1 chữ cái viết hoa
    //class tương tự như bản vẽ, đối tượng tương tự như ngôi nhà được xây dựng trên bản vẽ đó

    public static void main(String[] args) {
        //gõ tắt: psvm hoặc main
        //phương thức main là phương thức static, kiểu void
        //phương thức main chứa các phương thức khác, là nơi chương trình bắt đầu thực hiện
        //static: không cần có đối tượng vẫn có thể chạy được
        //phương thức: thể hiện hành động của đối tượng

        System.out.println("Hello World!");         //in ra màn hình, gõ tắt sout
        System.out.println("Toi la ai");
        System.out.println("Bui Tuan Anh");
    }
}

